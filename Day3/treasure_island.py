cross_road = input("You are at a cross road. Where do you want to go? type 'left' or 'right' ").lower()

if cross_road == 'left':
  swim_wait = input("swim or wait? ").lower()
  if swim_wait == 'swim':
    print("Attacked by trout. \nGame over:)")
  elif swim_wait == 'wait':
    door = input("Which door? type 'blue', 'yellow' or 'red' ").lower()
    
    if door == 'blue':
     print("Eating by beasts.\nGame over :)")
    elif door == 'red':
     print("Burned by fire.\n Game over :)")
    elif door == 'yellow':
     print("Congratulation. You win!!")
  else:
     print("Game over :)")
else:
  print("Fall into a hole. Game Over :)")


